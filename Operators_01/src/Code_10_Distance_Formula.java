public class Code_10_Distance_Formula {

	public static void main(String[] args) {
		int x1 = 2, y1 = 3, x2 = 10, y2 = 8;
		double distance = Math.sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));
		System.out.println("Distance: " + distance); // Print Calculated Distance
	}
}
