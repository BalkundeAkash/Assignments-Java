public class Code_03_bitwise_operators {

	public static void main(String[] args) {
		int a = 5; // Binary : 0101
		int b = 3; // Binary : 0011

		System.out.println("a & b: " + (a & b)); // AND Operation
		System.out.println("a | b:" + (a | b)); // OR Operation
		System.out.println("a ^ b:" + (a ^ b)); // XOR Operation

	}
}
