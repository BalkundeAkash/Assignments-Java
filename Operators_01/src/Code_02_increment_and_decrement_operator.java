public class Code_02_increment_and_decrement_operator {

	public static void main(String[] args) {
		int a = 10; // Initialize a with 10
		System.out.println("Initial a: " + a); // Display initial value of a
		System.out.println("Post-Increment: " + (a++)); // Post-Increment a , then Display it
		System.out.println("Pre-Increment: " + (++a)); // Pre-Increment a , then Display it
		System.out.println("Post-Decrement: " + (a--)); // Post-Decrement a , then Display it
		System.out.println("pre-Increment: " + (--a)); // Pre-Decrement a , then display it

	}

}
