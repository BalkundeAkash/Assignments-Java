public class Code_08_Compound_Assignment {
	public static void main(String[] args) {
		int num = 10;
		num += 5; // Equivalent to num = num + 5
		System.out.println("After += : " + num);
		num *= 2; // Equivalent to num = * 2
		System.out.println("After *= : " + num);
		num %= 3; // Equivalent to num = num % 3
		System.out.println("After %= : " + num);
	}
}
