public class Code_05_RelationalOperators {

	public static void main(String[] args) {
		int x = 10, y = 20;

		System.out.println("x == y:" + (x == y)); // Checks if x is equals to y
		System.out.println("x != y:" + (x != y)); // Check if x is not equal to y
		System.out.println("x > y:" + (x > y)); // Check if x is greater than y
		System.out.println("x < y:" + (x < y)); // Check if x is less than y
		System.out.println("x >= y:" + (x >= y)); // Check if x is greater than or equal to y
		System.out.println("x <= y:" + (x <= y)); // Check is x is less than or equal to y

	}
}
